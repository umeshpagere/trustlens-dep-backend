class TestComputeScore(unittest.TestCase):

    def test_no_signals_returns_baseline(self):
        score, factors = _compute_score(
            hash_matched=False,
            context_mismatch=False,
            metadata_present=True,
            editing_software_detected=False,
            ai_probability=0.0,
        )
        self.assertEqual(score, BASELINE_SCORE)
        self.assertEqual(factors, [])

    def test_reuse_penalty_applied(self):
        score, factors = _compute_score(
            hash_matched=True,
            context_mismatch=False,
            metadata_present=True,
            editing_software_detected=False,
            ai_probability=0.0,
        )
        self.assertEqual(score, BASELINE_SCORE - REUSE_PENALTY)
        self.assertTrue(any("known photograph" in f or "previously used" in f for f in factors))

    def test_context_mismatch_penalty_applied(self):
        score, factors = _compute_score(
            hash_matched=True,
            context_mismatch=True,
            metadata_present=True,
            editing_software_detected=False,
            ai_probability=0.0,
        )
        self.assertEqual(score, BASELINE_SCORE - REUSE_PENALTY - MISMATCH_PENALTY)
        self.assertTrue(any("mismatch" in f.lower() for f in factors))

    def test_missing_metadata_penalty(self):
        score, factors = _compute_score(
            hash_matched=False,
            context_mismatch=False,
            metadata_present=False,
            editing_software_detected=False,
            ai_probability=0.0,
        )
        self.assertEqual(score, BASELINE_SCORE - MISSING_METADATA_PENALTY)

    def test_editing_software_penalty(self):
        score, _ = _compute_score(
            hash_matched=False,
            context_mismatch=False,
            metadata_present=True,
            editing_software_detected=True,
            ai_probability=0.0,
        )
        self.assertEqual(score, BASELINE_SCORE - EDITING_SOFTWARE_PENALTY)

    def test_high_ai_probability_penalty(self):
        score, factors = _compute_score(
            hash_matched=False,
            context_mismatch=False,
            metadata_present=True,
            editing_software_detected=False,
            ai_probability=AI_HIGH_THRESHOLD + 0.1,
        )
        self.assertEqual(score, BASELINE_SCORE - AI_PROBABILITY_PENALTY)
        self.assertTrue(any("AI" in f for f in factors))

    def test_low_ai_probability_no_penalty(self):
        score, _ = _compute_score(
            hash_matched=False,
            context_mismatch=False,
            metadata_present=True,
            editing_software_detected=False,
            ai_probability=0.3,
        )
        self.assertEqual(score, BASELINE_SCORE)

    def test_all_penalties_clamped_to_zero(self):
        """All penalties combined must not go below 0."""
        score, _ = _compute_score(
            hash_matched=True,
            context_mismatch=True,
            metadata_present=False,
            editing_software_detected=True,
            ai_probability=0.9,
        )
        self.assertGreaterEqual(score, 0)

    def test_score_never_exceeds_100(self):
        score, _ = _compute_score(
            hash_matched=False,
            context_mismatch=False,
            metadata_present=True,
            editing_software_detected=False,
            ai_probability=0.0,
        )
        self.assertLessEqual(score, 100)


