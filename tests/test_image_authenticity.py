class TestImageUtils(unittest.TestCase):

    def test_valid_jpeg_returns_true(self):
        self.assertTrue(is_valid_image(_make_image_bytes()))

    def test_valid_png_returns_true(self):
        self.assertTrue(is_valid_image(_make_png_bytes()))

    def test_empty_bytes_invalid(self):
        self.assertFalse(is_valid_image(b""))

    def test_none_invalid(self):
        self.assertFalse(is_valid_image(None))

    def test_corrupt_bytes_invalid(self):
        self.assertFalse(is_valid_image(b"notanimage" * 10))

    def test_bytes_to_pil_valid(self):
        img = bytes_to_pil(_make_image_bytes())
        self.assertIsNotNone(img)
        self.assertIsInstance(img, Image.Image)

    def test_bytes_to_pil_invalid_returns_none(self):
        self.assertIsNone(bytes_to_pil(b"garbage"))

    def test_bytes_to_pil_none_input(self):
        self.assertIsNone(bytes_to_pil(None))

    def test_get_image_format_jpeg(self):
        fmt = get_image_format(_make_image_bytes())
        self.assertEqual(fmt, "JPEG")

    def test_get_image_format_png(self):
        fmt = get_image_format(_make_png_bytes())
        self.assertEqual(fmt, "PNG")

    def test_too_large_image_rejected(self):
        from app.utils.image_utils import MAX_IMAGE_BYTES
        oversized = b"x" * (MAX_IMAGE_BYTES + 1)
        self.assertIsNone(bytes_to_pil(oversized))


