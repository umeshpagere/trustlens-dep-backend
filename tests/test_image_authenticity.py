class TestEvaluateImage(unittest.TestCase):

    def test_none_input_returns_neutral(self):
        result = evaluate_image(None)
        self.assertEqual(result["imageAuthenticityScore"], BASELINE_SCORE)
        self.assertFalse(result["hashMatched"])

    def test_empty_bytes_returns_neutral(self):
        result = evaluate_image(b"")
        self.assertEqual(result["imageAuthenticityScore"], BASELINE_SCORE)

    def test_corrupt_bytes_returns_neutral(self):
        result = evaluate_image(b"notanimage" * 5)
        self.assertEqual(result["imageAuthenticityScore"], BASELINE_SCORE)
        self.assertTrue(len(result["riskFactors"]) > 0)

    def test_valid_image_no_hash_match_returns_score(self):
        with patch("app.services.image_authenticity_service.lookup_hash", return_value=None):
            result = evaluate_image(_make_image_bytes(), claim_context=None)
        self.assertIsNotNone(result["imageAuthenticityScore"])
        self.assertFalse(result["hashMatched"])

    def test_hash_match_applies_reuse_penalty(self):
        mock_entry = {
            "hash": "abc", "context": "Flood in Kerala 2019",
            "verifiedEventDate": "2019-08-12", "source": "The Hindu",
        }
        with patch("app.services.image_authenticity_service.lookup_hash", return_value=mock_entry):
            result = evaluate_image(
                _make_image_bytes(),
                claim_context="Russia Ukraine war 2022",   # clear mismatch
            )
        self.assertTrue(result["hashMatched"])
        self.assertEqual(result["matchedContext"], "Flood in Kerala 2019")
        self.assertLess(result["imageAuthenticityScore"], BASELINE_SCORE)

    def test_hash_match_same_context_no_mismatch(self):
        mock_entry = {
            "hash": "abc", "context": "Flood in Kerala 2019",
            "verifiedEventDate": "2019-08-12", "source": "The Hindu",
        }
        with patch("app.services.image_authenticity_service.lookup_hash", return_value=mock_entry):
            result = evaluate_image(
                _make_image_bytes(),
                claim_context="Kerala flood 2019 rescue operations",  # matches
            )
        self.assertTrue(result["hashMatched"])
        self.assertFalse(result["contextMismatch"])

    def test_output_structure_always_complete(self):
        """All required keys must be present on any input."""
        result = evaluate_image(_make_image_bytes())
        required = [
            "imageAuthenticityScore", "hashMatched", "matchedContext",
            "matchedEventDate", "contextMismatch", "aiGeneratedLikelihood",
            "metadataPresent", "cameraMake", "cameraModel",
            "editingSoftwareDetected", "riskFactors",
        ]
        for key in required:
            self.assertIn(key, result, f"Missing key: {key}")

    def test_unhandled_exception_returns_neutral(self):
        """evaluate_image must never raise — even on unexpected errors."""
        with patch("app.services.image_authenticity_service.is_valid_image", return_value=True), \
             patch("app.services.image_authenticity_service.compute_phash",
                   side_effect=RuntimeError("unexpected")):
            result = evaluate_image(_make_image_bytes())
        # Should get neutral score without crashing
        self.assertEqual(result["imageAuthenticityScore"], BASELINE_SCORE)


