class TestHashUtils(unittest.TestCase):

    def test_compute_phash_valid_image(self):
        result = compute_phash(_make_image_bytes())
        self.assertIsNotNone(result)
        self.assertIsInstance(result, str)
        self.assertEqual(len(result), 16)  # imagehash pHash default hex length

    def test_compute_phash_none_input(self):
        self.assertIsNone(compute_phash(None))

    def test_compute_phash_empty_bytes(self):
        self.assertIsNone(compute_phash(b""))

    def test_compute_phash_corrupt_bytes(self):
        self.assertIsNone(compute_phash(b"notanimage"))

    def test_identical_images_have_zero_distance(self):
        img_bytes = _make_image_bytes(color="green")
        h1 = compute_phash(img_bytes)
        h2 = compute_phash(img_bytes)
        self.assertEqual(phash_distance(h1, h2), 0)

    def test_different_images_have_nonzero_distance(self):
        h1 = compute_phash(_make_image_bytes(color="red"))
        h2 = compute_phash(_make_image_bytes(color="blue"))
        dist = phash_distance(h1, h2)
        self.assertIsNotNone(dist)
        # Different solid colours — distance typically > 0
        # (solid colour images may still have similar DCT, so just check not None)
        self.assertGreaterEqual(dist, 0)

    def test_phash_distance_none_inputs(self):
        self.assertIsNone(phash_distance(None, None))
        self.assertIsNone(phash_distance("abc", None))
        self.assertIsNone(phash_distance(None, "abc"))

    def test_is_similar_identical_hashes(self):
        img = _make_image_bytes(color="yellow")
        h = compute_phash(img)
        self.assertTrue(is_similar(h, h))

    def test_is_similar_with_none_returns_false(self):
        self.assertFalse(is_similar(None, None))
        self.assertFalse(is_similar("abc123", None))

    def test_is_similar_threshold_respected(self):
        img = _make_image_bytes(color="red")
        h = compute_phash(img)
        # Same hash — distance = 0 — always similar regardless of threshold
        self.assertTrue(is_similar(h, h, threshold=0))


