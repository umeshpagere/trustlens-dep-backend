class TestContextMismatch(unittest.TestCase):

    def test_matching_context_returns_false(self):
        """Same event tokens → no mismatch."""
        stored = "Flood in Kerala India 2019"
        claim = "The 2019 Kerala flood devastated the state"
        self.assertFalse(_detect_context_mismatch(stored, claim))

    def test_different_context_returns_true(self):
        """Completely different topic → mismatch."""
        stored = "Flood in Kerala India 2019"
        claim = "Russia Ukraine military operation 2022"
        self.assertTrue(_detect_context_mismatch(stored, claim))

    def test_empty_claim_returns_false(self):
        """Cannot assess mismatch without claim context."""
        self.assertFalse(_detect_context_mismatch("Flood in Kerala", ""))
        self.assertFalse(_detect_context_mismatch("Flood in Kerala", None))

    def test_short_claim_returns_false(self):
        """Claim shorter than MIN_CONTEXT_LENGTH → returns False safely."""
        self.assertFalse(_detect_context_mismatch("Flood in Kerala", "fire"))


