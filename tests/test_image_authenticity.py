class TestMetadataUtils(unittest.TestCase):

    def test_jpeg_without_exif_returns_not_present(self):
        img_bytes = _make_image_bytes()  # synthetic JPEG has no EXIF
        result = extract_exif(img_bytes)
        self.assertFalse(result["metadataPresent"])
        self.assertEqual(result["rawTagCount"], 0)

    def test_empty_bytes_returns_default(self):
        result = extract_exif(b"")
        self.assertFalse(result["metadataPresent"])

    def test_none_input_returns_default(self):
        result = extract_exif(None)
        self.assertFalse(result["metadataPresent"])

    def test_corrupt_bytes_returns_default(self):
        result = extract_exif(b"corruptdata")
        self.assertFalse(result["metadataPresent"])

    def test_png_returns_no_metadata(self):
        result = extract_exif(_make_png_bytes())
        self.assertFalse(result["metadataPresent"])  # PNG has no _getexif()

    def test_editing_software_detected_flag(self):
        """Mock _getexif to simulate Photoshop in EXIF Software tag."""
        fake_exif = {
            305: "Adobe Photoshop CS6",  # 305 = Software tag
            271: "Canon",                # 271 = Make
            272: "EOS 5D",              # 272 = Model
        }
        mock_img = MagicMock()
        mock_img._getexif.return_value = fake_exif

        with patch("PIL.Image.open", return_value=mock_img):
            result = extract_exif(_make_image_bytes())

        self.assertTrue(result["editingSoftwareDetected"])
        self.assertIn("photoshop", result["software"].lower())

    def test_clean_camera_software_not_flagged(self):
        fake_exif = {
            305: "Canon EOS Firmware 1.0",
            271: "Canon",
        }
        mock_img = MagicMock()
        mock_img._getexif.return_value = fake_exif

        with patch("PIL.Image.open", return_value=mock_img):
            result = extract_exif(_make_image_bytes())

        self.assertFalse(result["editingSoftwareDetected"])

    def test_output_has_all_required_keys(self):
        result = extract_exif(_make_image_bytes())
        for key in ["metadataPresent", "cameraMake", "cameraModel", "software",
                    "dateTimeOriginal", "gpsPresent", "editingSoftwareDetected", "rawTagCount"]:
            self.assertIn(key, result)


