def _make_image_bytes(width: int = 100, height: int = 100, color: str = "red") -> bytes:
    """Create a minimal in-memory JPEG for testing."""
    buf = io.BytesIO()
    Image.new("RGB", (width, height), color=color).save(buf, format="JPEG")
    return buf.getvalue()


def _make_png_bytes() -> bytes:
    buf = io.BytesIO()
    Image.new("RGB", (50, 50), color="blue").save(buf, format="PNG")
    return buf.getvalue()


